# visible marker for any glyph we forgot to draw
MISSING = ["#.#.#", ".#.#.", "#.#.#", ".#.#.", "#.#.#", ".#.#.", "#.#.#"]


def glyph_rows(ch):
    """8 rows of 8-bit ints (bit0 = leftmost pixel) from the 5x7 ASCII-art."""
    rows = GLYPHS.get(ch)
    if rows is None and 'a' <= ch <= 'z':
        rows = GLYPHS.get(ch.upper())           # lowercase -> uppercase glyph
    if rows is None:
        rows = ["....."] * 7                     # undefined codes -> blank
    out = []
    for r in list(rows)[:7]:
        r = r.ljust(6, '.')
        bits = 0
        for col, c in enumerate(r[:6]):
            if c == '#':
                bits |= 1 << col                 # bit0 = leftmost pixel
        out.append(bits)
    out.append(0)                                # row 7 blank
    return out


def emit(fg, bg):
    out = bytearray()
    for code in range(0x20, 0x80):
        for row in glyph_rows(chr(code)):
            for bx in range(0, 8, 2):
                hi = fg if (row >> bx) & 1 else bg          # bit0 = leftmost pixel
                lo = fg if (row >> (bx + 1)) & 1 else bg
                out.append((hi << 4) | lo)
    return out


